package com.myproject.BookManage.service.ex;

public class FindInfoException extends ServiceException {

	private static final long serialVersionUID = 9198208264864850138L;

	public FindInfoException() {
		super();
	}

	public FindInfoException(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	}

	public FindInfoException(String message, Throwable cause) {
		super(message, cause);
	}

	public FindInfoException(String message) {
		super(message);
	}

	public FindInfoException(Throwable cause) {
		super(cause);
	}

	
}
