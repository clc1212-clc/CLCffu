package com.myproject.BookManage.service.ex;

public class HaveExtraMoneyException extends ServiceException {

	private static final long serialVersionUID = -2566863903476998441L;

	public HaveExtraMoneyException() {
		super();
	}

	public HaveExtraMoneyException(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	}

	public HaveExtraMoneyException(String message, Throwable cause) {
		super(message, cause);
	}

	public HaveExtraMoneyException(String message) {
		super(message);
	}

	public HaveExtraMoneyException(Throwable cause) {
		super(cause);
	}

	
}
